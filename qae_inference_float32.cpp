// -------------------------- Basic complex + statevector utils --------------------------
struct Cx {
    float re;
    float im;
};

inline Cx cx_add(const Cx& a, const Cx& b) { return {a.re + b.re, a.im + b.im}; }
inline Cx cx_sub(const Cx& a, const Cx& b) { return {a.re - b.re, a.im - b.im}; }
inline Cx cx_mul_real(const Cx& a, float x) { return {a.re * x, a.im * x}; }

// (-i * s) * (re + i im) = (s*im) + i(-s*re)
inline Cx cx_mul_minus_i(const Cx& a, float s) { return {s * a.im, -s * a.re}; }

// e^{+i phi} * a
inline Cx cx_mul_phase_pos(const Cx& a, float c, float s) {
    return {a.re * c - a.im * s, a.re * s + a.im * c};
}

// e^{-i phi} * a
inline Cx cx_mul_phase_neg(const Cx& a, float c, float s) {
    return {a.re * c + a.im * s, -a.re * s + a.im * c};
}

template <int NQ>
struct StateVec {
    static constexpr int DIM = 1 << NQ;
    std::array<Cx, DIM> a{};

    void init_zero() {
        for (int i = 0; i < DIM; ++i) a[i] = {0.0, 0.0};
        a[0] = {1.0, 0.0};
    }
};

// -------------------------- LUT helpers --------------------------
inline int clamp_idx(int idx, int n) {
    if (idx < 0) return 0;
    if (idx >= n) return n - 1;
    return idx;
}

inline int lut_index_eta_phi(float theta) {
    const float lo = -M_PI;
    const float hi = +M_PI;
    float t = theta;
    if (t < lo) t = lo;
    if (t > hi) t = hi;
    float u = (t - lo) / (hi - lo);
    int idx = static_cast<int>(std::llround(u * (TRIG_LUT_SIZE - 1)));
    return clamp_idx(idx, TRIG_LUT_SIZE);
}

inline int lut_index_pt(float theta) {
    const float lo = 0.0;
    const float hi = M_PI;
    float t = theta;
    if (t < lo) t = lo;
    if (t > hi) t = hi;
    float u = (t - lo) / (hi - lo);
    int idx = static_cast<int>(std::llround(u * (TRIG_LUT_SIZE - 1)));
    return clamp_idx(idx, TRIG_LUT_SIZE);
}

inline void get_cs_eta_phi(float theta, float& c, float& s) {
    int idx = lut_index_eta_phi(theta);
    c = eta_phi_lut_cos[idx];
    s = eta_phi_lut_sin[idx];
}

inline void get_cs_pt(float theta, float& c, float& s) {
    int idx = lut_index_pt(theta);
    c = pt_lut_cos[idx];
    s = pt_lut_sin[idx];
}

// -------------------------- Gate kernels --------------------------
template <int NQ>
void apply_rx(StateVec<NQ>& sv, int wire, float c, float s) {
    const int stride = 1 << wire;
    constexpr int DIM = StateVec<NQ>::DIM;

    for (int base = 0; base < DIM; base += (stride << 1)) {
        for (int j = 0; j < stride; ++j) {
            int i0 = base + j;
            int i1 = i0 + stride;

            Cx a = sv.a[i0];
            Cx b = sv.a[i1];

            Cx ap = cx_add(cx_mul_real(a, c), cx_mul_minus_i(b, s));
            Cx bp = cx_add(cx_mul_minus_i(a, s), cx_mul_real(b, c));
            sv.a[i0] = ap;
            sv.a[i1] = bp;
        }
    }
}

template <int NQ>
void apply_ry(StateVec<NQ>& sv, int wire, float c, float s) {
    const int stride = 1 << wire;
    constexpr int DIM = StateVec<NQ>::DIM;

    for (int base = 0; base < DIM; base += (stride << 1)) {
        for (int j = 0; j < stride; ++j) {
            int i0 = base + j;
            int i1 = i0 + stride;

            Cx a = sv.a[i0];
            Cx b = sv.a[i1];

            Cx ap = cx_sub(cx_mul_real(a, c), cx_mul_real(b, s));
            Cx bp = cx_add(cx_mul_real(a, s), cx_mul_real(b, c));
            sv.a[i0] = ap;
            sv.a[i1] = bp;
        }
    }
}

template <int NQ>
void apply_rz(StateVec<NQ>& sv, int wire, float c, float s) {
    constexpr int DIM = StateVec<NQ>::DIM;
    const int bit = 1 << wire;

    for (int i = 0; i < DIM; ++i) {
        if ((i & bit) == 0) {
            sv.a[i] = cx_mul_phase_neg(sv.a[i], c, s);
        } else {
            sv.a[i] = cx_mul_phase_pos(sv.a[i], c, s);
        }
    }
}

template <int NQ>
void apply_cnot(StateVec<NQ>& sv, int control, int target) {
    if (control == target) throw std::runtime_error("CNOT control == target");
    constexpr int DIM = StateVec<NQ>::DIM;
    const int cb = 1 << control;
    const int tb = 1 << target;

    for (int i = 0; i < DIM; ++i) {
        if ((i & cb) && ((i & tb) == 0)) {
            int j = i | tb;
            std::swap(sv.a[i], sv.a[j]);
        }
    }
}

// -------------------------- Measurements --------------------------
template <int NQ>
float measure_expZ(const StateVec<NQ>& sv, int wire) {
    constexpr int DIM = StateVec<NQ>::DIM;
    const int bit = 1 << wire;
    float acc = 0.0;

    for (int i = 0; i < DIM; ++i) {
        const auto& z = sv.a[i];
        float p = z.re * z.re + z.im * z.im;
        acc += ((i & bit) ? -p : +p);
    }
    return acc;
}

