// Full C++ CPU-reference inference path for block_quantum_ae.py QAE anomaly model.
// Matches the architecture with standard particle assignments:
//
// - MET block (1 qubit): MET
// - Electrons block (4 qubits): e0, e1, e2, e3 (trash wires 2,3)
// - Muons block (4 qubits): mu0, mu1, mu2, mu3 (trash wires 2,3)
// - Jets block (10 qubits): j0-j9 (trash wires 6,7,8,9)
//
// - Encoding: RX(eta), RY(pt), RZ(phi)
// - Trainable layers: RY(weights)
// - Entangling: all-to-all CNOT between trash<->latent
// - Measurement: PauliZ on trash wires
// - Scoring: s_b = mean((1-Z)/2) per block
// - Mahalanobis score: 0.5 * (s-mu)^T Precision (s-mu)
//
// Requires generated headers from Python export step:
//   - qae_weights.h   (trainable RY weights and precomputed cos/sin)
//   - trig_luts.h     (pt and eta/phi LUTs: cos(theta/2), sin(theta/2))
//   - mahalanobis.h   (mu[4], precision[4][4])

