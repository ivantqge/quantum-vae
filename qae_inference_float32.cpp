// MET block: 1 qubit, particle = mu3
float run_met_block_z(const float x[56]) {
    StateVec<1> sv;
    sv.init_zero();

    const ParticleIdx& p = MET_BLOCK_PARTICLES[0];
    
    // qml.RY(pt, wires=0)
    {
        float c, s;
        get_cs_pt(x[p.pt], c, s);
        apply_ry(sv, 0, c, s);
    }

    // qml.RZ(phi, wires=0)
    {
        float c, s;
        get_cs_eta_phi(x[p.phi], c, s);
        apply_rz(sv, 0, c, s);
    }

    // trainable RY(weights[0])
    apply_ry(sv, 0, met_weights_c[0], met_weights_s[0]);

    return measure_expZ(sv, 0);
}

// Electrons block: 4 qubits, particles = e3, e2, j9, j8
std::array<float, 2> run_ele_block_z(const float x[56]) {
    StateVec<4> sv;
    sv.init_zero();

    // Feature map for k=0..3
    for (int k = 0; k < 4; ++k) {
        const ParticleIdx& p = ELE_BLOCK_PARTICLES[k];
        float c, s;
        
        // RX(eta) - use 0 if eta_idx is -1 (MET has no eta)
        if (p.eta >= 0) {
            get_cs_eta_phi(x[p.eta], c, s);
        } else {
            c = 1.0; s = 0.0;  // RX(0) = identity
        }
        apply_rx(sv, k, c, s);
        
        // RY(pt)
        get_cs_pt(x[p.pt], c, s);
        apply_ry(sv, k, c, s);
        
        // RZ(phi)
        get_cs_eta_phi(x[p.phi], c, s);
        apply_rz(sv, k, c, s);
    }

    // depth=1: trash -> latent all-to-all, RY(all), latent -> trash all-to-all
    const int latent[2] = {0, 1};
    const int trash[2]  = {2, 3};

    for (int ti = 0; ti < 2; ++ti) {
        for (int li = 0; li < 2; ++li) apply_cnot(sv, trash[ti], latent[li]);
    }

    for (int k = 0; k < 4; ++k) apply_ry(sv, k, ele_weights_c[k], ele_weights_s[k]);

    for (int li = 0; li < 2; ++li) {
        for (int ti = 0; ti < 2; ++ti) apply_cnot(sv, latent[li], trash[ti]);
    }

    return {measure_expZ(sv, 2), measure_expZ(sv, 3)};
}

// Muons block: 4 qubits, particles = mu1, mu2, j6, j2
std::array<float, 2> run_mu_block_z(const float x[56]) {
    StateVec<4> sv;
    sv.init_zero();

    for (int k = 0; k < 4; ++k) {
        const ParticleIdx& p = MU_BLOCK_PARTICLES[k];
        float c, s;
        
        if (p.eta >= 0) {
            get_cs_eta_phi(x[p.eta], c, s);
        } else {
            c = 1.0; s = 0.0;
        }
        apply_rx(sv, k, c, s);
        
        get_cs_pt(x[p.pt], c, s);
        apply_ry(sv, k, c, s);
        
        get_cs_eta_phi(x[p.phi], c, s);
        apply_rz(sv, k, c, s);
    }

    const int latent[2] = {0, 1};
    const int trash[2]  = {2, 3};

    for (int ti = 0; ti < 2; ++ti) {
        for (int li = 0; li < 2; ++li) apply_cnot(sv, trash[ti], latent[li]);
    }

    for (int k = 0; k < 4; ++k) apply_ry(sv, k, mu_weights_c[k], mu_weights_s[k]);

    for (int li = 0; li < 2; ++li) {
        for (int ti = 0; ti < 2; ++ti) apply_cnot(sv, latent[li], trash[ti]);
    }

    return {measure_expZ(sv, 2), measure_expZ(sv, 3)};
}

// Jets block: 10 qubits, particles = e1, j7, j5, e0, MET, mu0, j4, j0, j3, j1
std::array<float, 4> run_jet_block_z(const float x[56]) {
    StateVec<10> sv;
    sv.init_zero();

    // Feature map on all 10 wires
    for (int k = 0; k < 10; ++k) {
        const ParticleIdx& p = JET_BLOCK_PARTICLES[k];
        float c, s;
        
        if (p.eta >= 0) {
            get_cs_eta_phi(x[p.eta], c, s);
        } else {
            c = 1.0; s = 0.0;  // MET has no eta
        }
        apply_rx(sv, k, c, s);
        
        get_cs_pt(x[p.pt], c, s);
        apply_ry(sv, k, c, s);
        
        get_cs_eta_phi(x[p.phi], c, s);
        apply_rz(sv, k, c, s);
    }

    const int latent[6] = {0, 1, 2, 3, 4, 5};
    const int trash[4]  = {6, 7, 8, 9};

    // depth = 4
    for (int d = 0; d < 4; ++d) {
        for (int ti = 0; ti < 4; ++ti) {
            for (int li = 0; li < 6; ++li) apply_cnot(sv, trash[ti], latent[li]);
        }

        int base = d * 10;
        for (int k = 0; k < 10; ++k) {
            apply_ry(sv, k, jet_weights_c[base + k], jet_weights_s[base + k]);
        }

        for (int li = 0; li < 6; ++li) {
            for (int ti = 0; ti < 4; ++ti) apply_cnot(sv, latent[li], trash[ti]);
        }
    }

    return {
        measure_expZ(sv, 6),
        measure_expZ(sv, 7),
        measure_expZ(sv, 8),
        measure_expZ(sv, 9)
    };
}

// -------------------------- Scoring --------------------------
inline float trash_excitation_from_z(float z) {
    return 0.5 * (1.0 - z);
}

struct BlockOutputs {
    float met_z;
    std::array<float,2> ele_z;
    std::array<float,2> mu_z;
    std::array<float,4> jet_z;
};

BlockOutputs run_all_blocks(const float x[56]) {
    BlockOutputs o;
    o.met_z = run_met_block_z(x);
    o.ele_z = run_ele_block_z(x);
    o.mu_z  = run_mu_block_z(x);
    o.jet_z = run_jet_block_z(x);
    return o;
}

std::array<float,4> block_scores_from_outputs(const BlockOutputs& o, const float* presence_mask = nullptr) {
    float met_s = trash_excitation_from_z(o.met_z);

    float ele_s = 0.5 * (
        trash_excitation_from_z(o.ele_z[0]) +
        trash_excitation_from_z(o.ele_z[1])
    );

    float mu_s = 0.5 * (
        trash_excitation_from_z(o.mu_z[0]) +
        trash_excitation_from_z(o.mu_z[1])
    );

    float jet_s = 0.25 * (
        trash_excitation_from_z(o.jet_z[0]) +
        trash_excitation_from_z(o.jet_z[1]) +
        trash_excitation_from_z(o.jet_z[2]) +
        trash_excitation_from_z(o.jet_z[3])
    );

    std::array<float,4> s = {met_s, ele_s, mu_s, jet_s};

    if (presence_mask) {
        for (int i = 0; i < 4; ++i) s[i] *= presence_mask[i];
    }
    return s;
}

float mahalanobis_anomaly_score(const std::array<float,4>& s) {
    float d[4];
    for (int i = 0; i < 4; ++i) d[i] = s[i] - MAHA_MU[i];

    float q = 0.0;
    for (int i = 0; i < 4; ++i) {
        float row = 0.0;
        for (int j = 0; j < 4; ++j) row += MAHA_PREC[i][j] * d[j];
        q += d[i] * row;
    }
    return 0.5 * q;
}

float qae_anomaly_score_ref(const float x[56], const float* presence_mask = nullptr) {
    BlockOutputs bo = run_all_blocks(x);
    auto s = block_scores_from_outputs(bo, presence_mask);
    return mahalanobis_anomaly_score(s);
}

// -------------------------- CSV file reading --------------------------
#include <fstream>
#include <sstream>
#include <vector>
#include <string>
#include <iomanip>

std::vector<std::array<float, 56>> read_csv(const std::string& filename) {
    std::vector<std::array<float, 56>> data;
    std::ifstream file(filename);
    
    if (!file.is_open()) {
        std::cerr << "Error: Could not open " << filename << std::endl;
        return data;
    }
    
    std::string line;
    while (std::getline(file, line)) {
        std::array<float, 56> row;
        std::stringstream ss(line);
        std::string val;
        int col = 0;
        
        while (std::getline(ss, val, ',') && col < 56) {
            row[col++] = std::stod(val);
        }
        
        if (col == 56) {
            data.push_back(row);
        }
    }
    
    return data;
}

void write_scores_csv(const std::string& filename, 
                      const std::vector<std::array<float, 4>>& block_scores,
                      const std::vector<float>& anomaly_scores) {
    std::ofstream file(filename);
    file << std::fixed << std::setprecision(12);
    
    file << "met_score,ele_score,mu_score,jet_score,anomaly_score\n";
    for (size_t i = 0; i < anomaly_scores.size(); ++i) {
        file << block_scores[i][0] << "," 
             << block_scores[i][1] << "," 
             << block_scores[i][2] << "," 
             << block_scores[i][3] << ","
             << anomaly_scores[i] << "\n";
    }
    
    std::cout << "Wrote " << anomaly_scores.size() << " scores to " << filename << std::endl;
}

