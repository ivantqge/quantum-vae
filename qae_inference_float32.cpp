#include <iomanip>
#include <chrono>

int main(int argc, char* argv[]) {
    std::string input_file = "test_samples.csv";
    std::string output_file = "cpp_scores.csv";
    bool verbose = false;
    
    // Parse command line args
    for (int i = 1; i < argc; ++i) {
        std::string arg = argv[i];
        if (arg == "-i" && i + 1 < argc) {
            input_file = argv[++i];
        } else if (arg == "-o" && i + 1 < argc) {
            output_file = argv[++i];
        } else if (arg == "-v") {
            verbose = true;
        } else if (arg == "-h" || arg == "--help") {
            std::cout << "Usage: " << argv[0] << " [-i input.csv] [-o output.csv] [-v]\n";
            std::cout << "  -i  Input CSV file with 56 features per row (default: test_samples.csv)\n";
            std::cout << "  -o  Output CSV file for scores (default: cpp_scores.csv)\n";
            std::cout << "  -v  Verbose output (print each sample)\n";
            return 0;
        }
    }
    
    std::cout << "Loading data from " << input_file << "..." << std::endl;
    auto data = read_csv(input_file);
    
    if (data.empty()) {
        std::cerr << "No data loaded. Exiting." << std::endl;
        return 1;
    }
    
    std::cout << "Loaded " << data.size() << " samples" << std::endl;
    
    std::vector<std::array<float, 4>> all_block_scores;
    std::vector<float> all_anomaly_scores;
    all_block_scores.reserve(data.size());
    all_anomaly_scores.reserve(data.size());
    
    auto start = std::chrono::high_resolution_clock::now();
    
    for (size_t i = 0; i < data.size(); ++i) {
        BlockOutputs bo = run_all_blocks(data[i].data());
        auto s = block_scores_from_outputs(bo);
        float score = mahalanobis_anomaly_score(s);
        
        all_block_scores.push_back(s);
        all_anomaly_scores.push_back(score);
        
        if (verbose) {
            std::cout << "Sample " << i << ": "
                      << "block_scores=[" << s[0] << ", " << s[1] << ", " << s[2] << ", " << s[3] << "] "
                      << "anomaly=" << score << std::endl;
        }
    }
    
    auto end = std::chrono::high_resolution_clock::now();
    auto duration = std::chrono::duration_cast<std::chrono::milliseconds>(end - start);
    
    std::cout << "Inference completed in " << duration.count() << " ms" << std::endl;
    std::cout << "Throughput: " << (data.size() * 1000.0 / duration.count()) << " samples/sec" << std::endl;
    
    // Print summary stats
    float min_score = all_anomaly_scores[0], max_score = all_anomaly_scores[0], sum_score = 0;
    for (float s : all_anomaly_scores) {
        if (s < min_score) min_score = s;
        if (s > max_score) max_score = s;
        sum_score += s;
    }
    float mean_score = sum_score / all_anomaly_scores.size();
    
    std::cout << "\nAnomaly score statistics:" << std::endl;
    std::cout << "  Min:  " << min_score << std::endl;
    std::cout << "  Max:  " << max_score << std::endl;
    std::cout << "  Mean: " << mean_score << std::endl;
    
    // Write output
    write_scores_csv(output_file, all_block_scores, all_anomaly_scores);
    
    return 0;
}
